#entrada
numero_1 = int(input("Digite um numero: "))
numero_2 = int(input("Digite outro numero: "))
numero_3 = int(input("Digite só mais um numero: "))
#processamento
soma_dos_primeiros = numero_1 + numero_2
diferenca_dos_ultimos = numero_2 - numero_3
#saida
print(f"A soma dos dois primeiros numeros é {soma_dos_primeiros} e a diferença entre os dois ultimos é {diferenca_dos_ultimos}")